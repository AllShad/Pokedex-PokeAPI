export function colorTypeInternal(type){    
    switch(type){
        case 'normal':
            return '#B7B7A9'
        case 'fighting':
            return '#C56E60'
        case 'flying':
            return '#9AA9FF'
        case 'poison':
            return '#B76FA9'
        case 'ground':
            return '#E2C56F'
        case 'rock':
            return '#C5B77D'
        case 'bug':
            return '#B7C544'
        case 'ghost':
            return '#7D7DC5'
        case 'steel':
            return '#B7B7C5'
        case 'fire':
            return '#FF6144'
        case 'water':
            return '#52A9FF'
        case 'grass':
            return '#8CD46F'
        case 'electric':
            return '#FFD351'
        case 'psychic':
            return '#FF6FA9'
        case 'ice':
            return '#7CD3FF'
        case 'dragon':
            return '#8C7DF1'
        case 'dark':
            return '#8B6E60'
        case 'fairy':
            return '#F1A8F1'
        case 'unknown':
            return '#98C4B8'
        case 'shadow':
            return '#000000'
    }
}